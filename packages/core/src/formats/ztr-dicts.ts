/**
 * Key-dictionary tables for the ZTR text container (see {@link ../formats/ztr.js}).
 *
 * The FFXIII trilogy encodes control glyphs (colours, button icons, accented
 * Latin characters, special tokens) as one- or two-byte sequences inside each
 * decoded message line. The original NovaChrysalia decoder/encoder
 * (NovaChrysalia.Formats.ZTRHelpers.Support.KeyDictionaries.KeyDicts{Cmn,XIII,
 * XIII2,XIII3}) maps those byte sequences to a human-readable `{Token}` form
 * and back. This module reproduces those tables verbatim, extracted from the
 * decompiled C# static initialisers.
 *
 * Two-byte keys are packed as a single number `(b1 << 8) | b2`.
 *
 * Only the codepage-932 (Latin/Japanese, "LJ") path is reproduced here, which
 * is the path used for every Western release. The Chinese (cp950) and Korean
 * (cp51949) variants (`Ch*`/`Kr*` tables in the original) are not needed for
 * the LJ path and are omitted.
 *
 * THIS FILE IS DERIVED DATA — do not hand-edit. See the module header of
 * {@link ../formats/ztr.js} for how the tables are consumed.
 */

/** Game selector used to pick the per-title colour/icon/button tables. */
export type ZtrGameCode = 1 | 2 | 3;

/** Pack a two-byte control sequence `(b1, b2)` into a single map key. */
export function packPair(b1: number, b2: number): number {
  return ((b1 << 8) | b2) >>> 0;
}

